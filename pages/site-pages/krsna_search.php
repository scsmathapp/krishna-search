		<h1>Afterword</h1>
		
		<p>When Śrīla Bhakti Sundar Govinda Dev-Goswāmī Mahārāj published Śrī Gauḍīya Darśan, he headlined the cover page of each issue with the poem of Śrīla Bhakti Rakṣak Śrīdhar Dev-Goswāmī Mahārāj which ‘encaged’ the Lion-guru, Śrīla Bhakti Siddhānta Saraswatī Ṭhākur:</p>
		
		<p class="verse">nikhila-bhuvana-māyā-chhinna-vichchhinna-kartrī
			<br />vibudha-bahula-mṛgyā-mukti-mohānta-dātrī
			<br />śithilita-vidhi-rāgārādhya-rādheśa-dhānī
			<br />vilasatu hṛdi nityaṁ bhakti-siddhānta-vāṇī</p>
		
		<p>“May the demolisher of the entire world’s illusion, the finisher of the intellectuals’ mad pursuit of liberation, the abode of pure devotion to Śrī Rādhā’s beloved Lord, the message of Śrī Bhakti Siddhānta, play within our hearts eternally.”</p>
		
		<p>This prayer illustrates the purpose for which Śrī Chaitanya Sāraswat Maṭh was founded: to instil Śrī Bhakti Siddhānta vāṇī in every heart.</p>
		
		<p>While Śrī Bhakti Siddhānta vānī primarily means ‘the teachings of Śrīla Bhakti Siddhānta Saraswatī Ṭhākur’, Śrīla Govinda Mahārāj has noted that it also refers to the teachings of pure devotion given in the Rūpānuga sampradāya as a whole—the ultimate understanding (siddhānta) of devotion (bhakti). Śrī Bhakti Siddhānta vānī can also be considered a veiled reference to the nectar Śrīman Mahāprabhu infused within, and heard from, Śrīla Rāmānanda Rāy on the bank of the Godāvarī (<em>sva-bhakti-siddhānta-chayāmṛtāni</em>; Cc: 2.8.1). Overall, the phrase represents the spirit of the Maṭh and the sampradāya; it is the flag which stands for the shared aspiration of the faithful, the divine voice heard within the prayerful heart.</p>
		
		<p>The Rūpānuga āchāryas’ teachings instil the Lord’s divine power within the surrendered souls’ hearts. They are the perpetual source of the protection, hope, purpose, vigour, and joy which the surrendered souls depend on in every day of their lives. They are the Lord’s abounding, unconditional mercy, and the sole light within māyā’s darkness. They are doom for the egotism and doubt that attack the weak-hearted. They are the arsenal of explosives in the war against māyā, which are thrown externally at the misconceptions of the deluded masses and internally at the mind’s phantasmagoria. They are like the razor edge of the sacrificial knife used to cut away everything unwholesome. They dispel all evil and reveal themselves to be the epitome of benevolence. As a great soul may sometimes be hard as a thunderbolt and at other times soft as a rose, so his words may at times jarringly strike down, and at others soothe with compassionate affection. The Rūpānuga Āchāryas’ teachings attract our intelligence, capture our hearts, and lead us home. They solve our problems, fulfil our desires, and surcharge us with the joy of divine service.</p>
		
		<p>As one’s appreciation for Śrī Bhakti Siddhānta vāṇī deepens, so one’s inspiration to dedicate one’s life to its directives and distribution increases. The <em>Sāraswat Style Manual</em> has been compiled to unite devotees in carrying on this eternal pursuit, and to help them do so in the manner preferred by the Āchāryas.</p>
		
		<p>In the beginning of <em>Śrī Bhakti-rasāmṛta-sindhu</em> (1.1.2), Śrīla Rūpa Goswāmī Prabhu shows his followers the conviction, and the humility, with which such work must be undertaken.</p>
		
		<p class="verse">hṛdi yasya preraṇayā pravartito ‘haṁ varāka-rūpo ‘pi
			<br />tasya hareḥ pada-kamalaṁ vande chaitanya-devasya</p>
		
		<p>“I offer my obeisance to the lotus feet of the Lord, Śrī Chaitanyadev, by whose inspiration in my heart I have undertaken this service, though I am unqualified.”</p>
		
		<p>The scriptures state that they are perfect and encourage everyone to study them with such faith.</p>
		
		<p class="verse">bhrama, pramāda, vipralipsā, karaṇāpāṭava
			<br />ārṣa-vijña-vākye nāhi doṣa ei saba
			<br />īśvarera vākye nāhi doṣa ei saba
			<br /><span class="verse-reference">(Śrī Chaitanya-charitāmṛta: Ādi-līlā, 2.86; 7.107)</span></p>
		
		<p>“The faults of misunderstanding, inattentiveness, deceit, and sensory deficiency do not occur in the statements of the realised sages or the Lord.”
	While this is so, the faults the scriptures are free from constantly occur in the life of every conditioned soul. As Param Guru Mahārāj wrote, “To err is human”. The gulf between divine perfection and human endeavour often seems impassable. Diligently proofreading a text to the best of one’s ability and then reviewing the long list of mistakes found within it by others thereafter vividly demonstrates ‘before one’s eyes’ the effect of māyā’s conditioning. Yet in the scriptures solace can be found: in <em>Śrī Śrī Prapanna-jīvanāmṛtam</em> Śrīla Śrīdhar Mahārāj cites a reassuring verse from Nārad’s chastisement of Vyās:</p>
		
		<p class="verse">tad vāg-visargo janatāgha-viplavo
			<br />yasmin prati-ślokam abaddhavaty api
			<br />nāmāny anantasya yaśo ’ṅkitāni yat
			<br />śṛṇvanti gāyanti gṛṇanti sādhavaḥ
			<br /><span class="verse-reference">(Śrīmad Bhāgavatam: 1.5.11)</span></p>
		
		<p>“Literature that contains the Infinite Lord’s glorious Names, which the sādhus hear, sing, and preach, destroys the people’s sins, even if every verse within it contains errors.”</p>
		
		<p>If Kṛṣṇa wills that souls will realise, they will do so, irrespective of the language they may read in the process. Kṛṣṇa is the actor; His devotees are His instruments whom He uses to distribute Himself, to the extent that He desires.</p>
		
		<p>Proper understanding of this truth demolishes ego. It checks one from being inhibited by one’s shortcomings and from being carried away by ‘one’s own brilliance’. Furthermore, it reminds everyone why Śrīman Mahāprabhu refers to kavitā, learning, as an anartha in His <em>Śikṣāṣṭakam</em>.</p>
		
		<p>Be that as it may, devotees are never satisfied with lacklustre service to their Beloved. The depth of their desire to satisfy the Lord drives them to endeavour to their utmost to present the Word as perfectly as Providence allows. Extreme zeal is their trademark:</p>
		
		<p class="verse">rādhā-mukunda-pada-sambhava-gharma-bindu-
			<br />nirmañchanopakaraṇī-kṛta-deha-lakṣām
			<br /><span class="verse-reference">(Śrī Lalitāṣṭakam: 1)</span></p>
		
		<p>“He is eager to take birth 100,000 times to remove one drop of sweat from the lotus feet of Rādhā-Mukunda.”</p>
		
		<p>With this verse Śrīla Rūpa Goswāmī Prabhu glorified Śrīla Svarūp Dāmodar, he who reviews all literature before it is heard by Śrīman Mahāprabhu.
	The task of publishing Śrī Bhakti Siddhānta vāṇī is a very difficult one: it requires, in addition to strong sukṛti, extreme scrutiny, responsibility, humility, and vigour. Moreover, it is infinite: on one hand the Lord’s Pastimes are ever-new and Ananta Śeṣa never nears their end, and on the other time is always passing and māyā’s influence alters the teachings, the languages in which they are coded, and the cultural contexts in which they are explained. The necessity for restating the teachings is perpetual, as is the necessity of preserving their authenticity. Towards this end, all the elements of style—spelling, grammar, syntax, typography, graphic design, etc.—need to be employed to their full potential.</p>
		
		<p>Literature is the primary tool in the Āchāryas’ endeavours to fulfil Śrīman Mahāprabhu’s prophecy that His Name will spread to every town and village of the world. Simultaneously, it is their primary tool to preserve the integrity of Mahāprabhu’s sampradāya as it expands. Literature is even more than important than the institutions and centres in which it is studied: once when the resources of his Mission were being misappropriated, Śrīla Saraswatī Ṭhākur declared that the Mission’s physical property could be sold to fund the printing and distribution of its literature. By saying so, he indicated that extreme care should be taken to ensure that during the course of maintaining the Mission, the ideal for which the Mission stands, the ideal which the literature establishes, is not neglected. Śrīla Śrīdhar Mahārāj also explained that the importance of literature can outweigh even the literal practice of kīrtan:</p>
		
		<p>When a mṛdaṅga resounds, it invites the people around it, “Come to hear the chanting of the Divine Name.” Similarly, books go from man to man, house to house, informing people, “These things are occurring. Come and join.” The ‘sound’ made by books, however, works more efficiently than the sound made by the mṛdaṅga. So our Guru Mahārāj called them the bṛhat mṛdaṅga. The inundation upon people made by books is more intense and more perfect than that made by the mṛdaṅga.</p>
		
		<p>In this day and age the bṛhat mṛdaṅga has taken a digital form. Websites and ebooks are now the most efficient means to distribute the Āchāryas’ message. Printed publications still play an important role though: they act as a standard of authenticity for compositions and can convey an elegance that is unattainable on screen. Thus both printed and digital publications are integral to preserving the Āchāryas’ message, and its vitality, into posterity.</p>
		
		<p>All things considered, the success of the <em>Sāraswat Style Manual</em> and the endeavours of the servants of Śrī Bhakti Siddhānta vāṇī is measured not only by the message’s clarity and longevity, but by the transformative power it exerts over the hearts of its audience. While making the message available to the greatest number of people and systematically archiving the maximum number of its expressions are important undertakings, facilitating the message entering into the depths of even a few hearts is the foremost service to the message. And although success in this regard may ultimately result in the hard work that goes into preserving, formatting, and styling the message being overlooked, as it did when Gadādhar Paṇḍit’s tears washed away his Bhāgavatam while Śrīman Mahāprabhu relished the extremes of Kṛṣṇa-prema, the servants of Śrī Bhakti Siddhānta vāṇī consider such ecstatic ‘disregard’ the perfection of their endeavours.</p>
